//hay tres formas de crear una variable
//var = fue la forma original de crear una variable puede ser accesible por cualquier lado aunque no se le haya asignado un valor
var helloWorld = "¡Hello, JavaScript!";
console.log(helloWorld);

//let solo son accesibles dentro de un bloque
let helloWorld2 = "¡Hola, JavaScript 2!";
console.log(helloWorld2);

//const no se puede reasignar el valor
const helloWorld3 = "¡Hola, JavaScript 3!";
console.log(helloWorld3);